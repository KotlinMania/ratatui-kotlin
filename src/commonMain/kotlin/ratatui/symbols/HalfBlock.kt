package ratatui.symbols

/**
 * Half block characters for drawing with 1x2 resolution per cell.
 */
object HalfBlock {
    /** Upper half block ▀ */
    const val UPPER: Char = '▀'
    /** Lower half block ▄ */
    const val LOWER: Char = '▄'
    /** Full block █ */
    const val FULL: Char = '█'
}
