package ratatui.symbols.shade

/**
 * Shade characters for filling areas with different densities.
 */
object Shade {
    /** Empty (no shade) */
    const val EMPTY = " "
    /** Light shade (░) */
    const val LIGHT = "░"
    /** Medium shade (▒) */
    const val MEDIUM = "▒"
    /** Dark shade (▓) */
    const val DARK = "▓"
    /** Full shade (█) */
    const val FULL = "█"
}
